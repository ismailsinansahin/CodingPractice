public class HelloWorld {

    public static void main(String[] args) {

        int i = 11;

        switch (i){
            case 6:
                System.out.println("6");
                break;
            case 7:
                System.out.println("7");
                break;
            case 8:
                System.out.println("8");
                break;
            default:
                System.out.println("not found!");
                break;
        }

    }

}


